export interface APILocation {
  id: string;
  countryName: string;
  name: string;
  lat: string;
  lng: string;
  countryCode: string;
  continentName: string;
  createdDate: string;
}

const getRandomFloat = (min: number, max: number, decimalPlaces: number) => {
  const random = Math.random() * (max - min) + min;
  const multiplier = 10 ** decimalPlaces;
  return Math.round(random * multiplier) / multiplier;
};

const getRandomDate = (startDate: Date, endDate: Date) => {
  const startTimestamp = startDate.getTime();
  const endTimestamp = endDate.getTime();
  const randomTimestamp =
    startTimestamp + Math.random() * (endTimestamp - startTimestamp);

  const randomDate = new Date(randomTimestamp);
  return randomDate;
};

export const getListOfRandomLocations = (
  numberOfLocations: number
): APILocation[] => {
  const locationsList = [];

  for (let i = 0; i < numberOfLocations; i++) {
    const lat = getRandomFloat(-90, 90, 6).toString();
    const lng = getRandomFloat(-180, 180, 6).toString();

    locationsList.push({
      id: i.toString(),
      countryName: `Address`,
      name: `Location ${i}`,
      countryCode: i.toString(),
      createdDate: getRandomDate(
        new Date('2020-01-01'),
        new Date('2023-10-15')
      ).toISOString(),
      lat,
      lng,
      continentName: '',
    });
  }

  return locationsList;
};

